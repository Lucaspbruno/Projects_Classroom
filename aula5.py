"""
Conversao de dados 
quando se converte dados é usado algo chamado coerçao onde se coloca o tipo da variavel que deseja na frente
da variavel que deseja converter. Exemplo - print(int('2')+1)

No caso de conversao de uma variavel do tipo boolean é um pouco diferente pois quando se converte
uma variavel do tipo inteiro, float ou string para boolean este apenas aponta se o valor True se há algo 
dentro da variavel ou False se nao há nenhum caracter dentro.

"""

print(int('1')+ 1) #coversao de dados.
print(bool('1')) #conversao para o tipo boolean com valor True.
print(bool('')) #conversao para o tipo boolean com valor False.

#Obs: variaveis do tipo string nao podem ser convertidas para o tipo int ou float.




