"""
Como criar nomes de variaveis em Python

segundo as normas uma variavel sempre deve começar com letra minuscula e deve conter '_' entre os espaços,
nomes de variaveis podem conter valores ou podem ser atribuidos valores a elas.
Sempre coloque nomes de variveis que estejam relacionadas ao conteudo delas.

"""

nome = 'Lucas' # tipo de variavel string.
idade = 28 # tipo de variavel inteiro.
maior_de_idade = idade >= 18 # variavel do tipo boolean.

print('Nome:',nome,',','Idade:',idade,',','Maior de Idade?',maior_de_idade)

