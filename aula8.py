"""
Soma, subtraçao, multiplicaçao e divisao em python

use a funçao print() com variaveis do tipo inteiro ou float onde se acrescenta o sinal correspondente 
a equaçao entre elas, e esta te retornara o valor.

Obs: A divisao sempre retornara um numero do tipo float. Se for uma divisao de numeros inteiros ele cortara
todas as casas decimais.

há tambem a exponencicao que pode ser executada atraves de '**' onde um numero sera elavado a outro.
Por fim tambem existe o resto da divisao que consiste em exibir o valor que sobrou da divisao e isto pode 
ser feito atraves do sinal'%' entre dois numeros.

"""

print(2+2) #soma
print(2-1) #subtraçao
print(2*3) #multiplicacao
print(2/3) #divisao
print(2//3) #divisao inteira
print(2**9) #exponenciacao
print(25%4) #resto da divisao
